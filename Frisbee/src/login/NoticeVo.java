package login;

public class NoticeVo {

	private String no;
	private String content;
	private String storeNo;
	
	public NoticeVo(String no, String content) {
		this.no = no;
		this.content = content;
		
	}
	
	
	public String getNo() {
		return no;
	}
	public String getContent() {
		return content;
	}
	public String getStoreNo() {
		return storeNo;
	}
	
	
	
}
